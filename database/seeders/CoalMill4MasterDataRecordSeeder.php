    private function parseRows(string $raw): array
    {
        $lines = preg_split('/\r\n|\r|\n/', trim($raw));
        array_shift($lines);

        $rows = [];

        foreach ($lines as $line) {
            if (trim($line) === '') {
                continue;
            }

            $columns = explode("\t", $line, 5);
            $columns = array_pad($columns, 5, null);

            $rows[] = $columns;
        }

        return $rows;
    }

    private function nullableValue(mixed $value): ?string
    {
        $value = trim((string) $value);

        if ($value === '' || strtoupper($value) === 'N/A' || $value === '-') {
            return null;
        }

        return $value;
    }
